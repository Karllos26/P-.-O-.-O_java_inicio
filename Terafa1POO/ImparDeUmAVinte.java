package tarefa;


public class ImparDeUmAVinte {

	/*4.Elaboreuma  aplicação  que  apresentetodos  
	 os  valores  numéricos  ímpares situados na 
	 faixa de 0 a 20. 
	 */
	
	public static void main(String[] args) {
		
		for(int impar = 1; impar <= 20; impar += 2) {
			System.out.println(impar);
			
		}
	}
	
}
